def load_inventory(filepath):
    """Load and validate the hosts.yml inventory file."""
    if not filepath.exists():
        print(f"❌ ERROR: Inventory file not found: {filepath}", file=sys.stderr)
        sys.exit(1)
    
    with open(filepath, 'r') as f:
        inventory = yaml.safe_load(f)
    
    if not inventory or 'production' not in inventory:
        print(f"❌ ERROR: Invalid inventory format (missing 'production' key)", file=sys.stderr)
        sys.exit(1)
    
    print(f"✅ Loaded inventory from {filepath}")
    return inventory['production']


def render_terraform_tfvars(inventory):
    """Generate Terraform tfvars from inventory."""
    tfvars = {
        # VIP configuration
        "production_vip_ip": inventory['vip']['ip'],
        "production_vip_fqdn": inventory['vip']['fqdn'],
        
        # Primary host configuration
        "primary_host_ip": inventory['hosts']['primary']['ip'],
        "primary_host_fqdn": inventory['hosts']['primary']['fqdn'],
        "primary_ssh_user": inventory['hosts']['primary']['ssh_user'],
        "primary_ssh_port": inventory['hosts']['primary']['ssh_port'],
        "primary_roles": inventory['hosts']['primary']['roles'],
        
        # Replica host configuration
        "replica_host_ip": inventory['hosts']['replica']['ip'],
        "replica_host_fqdn": inventory['hosts']['replica']['fqdn'],
        "replica_ssh_user": inventory['hosts']['replica']['ssh_user'],
        "replica_ssh_port": inventory['hosts']['replica']['ssh_port'],
        "replica_roles": inventory['hosts']['replica']['roles'],
        
        # Network configuration
        "network_subnet_cidr": inventory['networks']['prod_subnet']['cidr'],
        "network_gateway": inventory['networks']['prod_subnet']['gateway'],
        
        # DNS configuration
        "dns_domain": inventory['dns']['domain'],
        "dns_coredns_port": inventory['dns']['coredns_port'],
    }
    
    # Convert to HCL format
    hcl_lines = [
        "# Terraform variables generated from environments/production/hosts.yml",
        "# DO NOT EDIT THIS FILE — Update hosts.yml and re-run: python3 scripts/render-inventory-templates.py",
        f"# Generated: {datetime.now().isoformat()}",
        "",
    ]
    
    for key, value in tfvars.items():
        if isinstance(value, str):
            hcl_lines.append(f'{key} = "{value}"')
        elif isinstance(value, (int, float)):
            hcl_lines.append(f'{key} = {value}')
        elif isinstance(value, list):
            json_list = json.dumps(value)
            hcl_lines.append(f'{key} = {json_list}')
        elif isinstance(value, dict):
            json_dict = json.dumps(value)
            hcl_lines.append(f'{key} = {json_dict}')
    
    return "\n".join(hcl_lines)


